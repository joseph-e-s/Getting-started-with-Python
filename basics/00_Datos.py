print(type(1)) # Tipo int -> Numeros enteros o exactos
print(type(1.2)) # Tipo float -> Numeros decimales
print(type(1+2j)) # Tipo complejo -> Este es especial, por el momento no hay que darle mucha atencion
print(type(True)) # Tipo boleano -> Nos dice si es Verdadero o Falso




